package anudeep;
//Array is a linear data structure which stores elements of same kind.
//Elements of array gets stored in contiguous way.
//Array indexing starts with 0;
//syntax:
//int a[]=new int[5];
//array element is recognized by its index number;

public class ArrayDemo {
public static void main(String[] args) {
	//Static declaration of elements...
	int a[]= {45,43,85,63};
	int i;
	System.out.println(0);
	System.out.println(2);
	System.out.println("for printing multiple values.");
	for(i=0;i<4;i++) {
		System.out.println(a[i]);
	}
}
}
